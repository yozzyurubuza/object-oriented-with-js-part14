'use strict';

const Car = function (make, speed) {
  this.make = make;
  this.speed = speed;
};

Car.prototype.accelerate = function () {
  this.speed += 10;
  console.log(`${this.make} is going at ${this.speed} km/h`);
};

Car.prototype.brake = function () {
  this.speed -= 5;
  console.log(`${this.make} is going at ${this.speed} km/h`);
};

const EV = function (make, speed, charge) {
  Car.call(this, make, speed);
  this.charge = charge;
};

EV.prototype = Object.create(Car.prototype);
EV.prototype.constructor = EV;

EV.prototype.chargeBattery = function (chargeTo) {
  this.charge = chargeTo;
};

//Polymorphism - Changing the inherited function from parent class
EV.prototype.accelerate = function () {
  this.speed += 20;
  this.charge -= 1;
  console.log(
    `${this.make} is going at ${this.speed} km/h, with a charge of ${this.charge}%`
  );
};

const tesla = new EV('Tesla', 120, 23);

//The first one that appears in the prototype chain will be the one that will be used
tesla.accelerate();
tesla.accelerate();
tesla.accelerate();
tesla.accelerate();

// Brake doesn't display charge because we didn't polymorph the method from parent class
tesla.brake();
tesla.brake();
tesla.brake();

tesla.chargeBattery(90);

tesla.accelerate();
tesla.accelerate();
tesla.accelerate();
tesla.accelerate();

